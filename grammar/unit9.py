# 9 문자열 사용하기

hello = '''Hello, World!
안녕하세요
Python입니다.'''

print(hello)
#여러줄인 변수 할당 법 ''', """"

single_quote = '''"안녕하세요."
'파이썬'입니다.'''

double_quote1 = """"Hello"
'Python'"""

double_quote2 = """hello, 'Python'"""

print(single_quote)
print(double_quote1)
print(double_quote2)

